age_columns = "59세 이하,60-64세,65-69세,70-79세,80-89세,90세 이상".split(',')
df[age_columns] = df[age_columns].apply(pd.to_numeric, errors='coerce').astype('Int64')

nan_mask = df["실인원"].isna()
