def in_range(code, start, end):
    c, s, e = code[:3], start[:3], end[:3]
    # 동일 알파벳이면 숫자끼리 비교
    if s[0]==e[0]==c[0]:
        return s[1:].isdigit() and c[1:].isdigit() and e[1:].isdigit() and s[1:]<=c[1:]<=e[1:]
    # 아니면 문자열 비교
    return s <= c <= e

def map_by_icd_ranges(code):
    code = code.strip()[:3]
    for s, e, depts in icd_ranges:
        if in_range(code, s, e):
            return depts[0]  # 목록 중 첫 번째를 대표로
    return None

