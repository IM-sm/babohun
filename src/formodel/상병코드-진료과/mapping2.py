# 4) 우선 map_df로 매핑, 없으면 icd_ranges로 매핑, 그래도 없으면 '미분류'
def get_dept(code):
    if code in dept_map:
        return dept_map[code]
    r = map_by_icd_ranges(code)
    return r if r is not None else '미분류'

df['대표진료과'] = df['상병코드'].apply(get_dept)

